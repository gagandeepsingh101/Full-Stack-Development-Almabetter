// Primitive Data Types

// String
var num=10;
console.log(num);
console.log(typeof num);

// String
var str="Hello World";
console.log(str);
console.log(typeof str);

// Boolean
var bool=5;
console.log(bool);
console.log(typeof bool);

// undefined
var notValue;
console.log(notValue);

// Null
var NullValue=null;
console.log(typeof NullValue);


// Non-Primitive Data Types

// Object 
var obj_val={
    firstName:"Gagandeep",
    SeconfName:"Singh"
}
console.log(obj_val);
console.log(typeof obj_val);

// Array
var names = ["Mayank","Shuban","Amrita"];
console.log(names);
console.log(typeof names);

// Function
function sum(a,b){
    return a + b;
}
console.log(sum(2,4));
console.log(typeof sum(2,4));
